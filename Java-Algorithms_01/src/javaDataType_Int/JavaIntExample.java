package javaDataType_Int;

// Java int example
// This program shows how to declare and use Java primative int variable inside a java class

public class JavaIntExample {
	
	public static void main(String[] args) {
	
	/*
	 * int is a 32 bit signed type ranges from -2,147,483,648 to 2,147,483,647.
	 * int is also most commonly used integer type in Java
	 * 
	 * Declare int variable as shown below
	 * 
	 * int <variable name> = <default value>;
	 * 
	 * here assigning default value is optional
	 * 
	 */
	
	int i = 0;
	int j = 100;
	
	System.out.println("Value of int variable i is: " + i);
	System.out.println("Value of int variable j is: " + j);	
	}
}

/*
 * Output would be
 * Value of int variable i is: 0
 * Value of int variable j is: 100
 */
