package javaDataType_Double;

// Java double example
// This program shows how to declare and use Java primitive double variable inside a java class

public class JavaDoubleExample {
	
	public static void main(String[] args){
		
		/*
		 * double is a 64 bit double precision type and used when 
		 * fractional precision calculation is required
		 * 
		 * Declare double variable as show below
		 * 
		 * double <variable name> = <default value>;
		 * 
		 * here assigning default value is optional
		 * 
		 */
		
		double d = 1232.44;
		System.out.println("Value of double variable d is: " + d);
	}
}

// Output would be
// Value of double variable d is: 1232.44