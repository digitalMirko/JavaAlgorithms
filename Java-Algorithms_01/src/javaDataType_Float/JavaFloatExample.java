package javaDataType_Float;

// Java float example
// This program shows how to declare and use Java primative
// float variable inside a java class

public class JavaFloatExample {
	
	public static void main(String[] args) {
		
		/*
		 * float is 32 bit single precision type and used when frational
		 * precision calculation is required
		 * 
		 * Declare float variable as below
		 * 
		 * float <variable name> = <default value>;
		 * 
		 * here assigning default value is optional
		 * 
		 */
		
		float f = 10.4f;
		System.out.println("Value of float variable f is: " + f);
	}
}

// Output would be
// Value of float variable f is: 10.4